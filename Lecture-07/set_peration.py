set1 = {1, 2, 3}
set2 = {3, 4, 5}

print(set1.union(set2))  # Union of set1 and set2

print(set1.intersection(set2))  # Intersection of set1 and set2

print(set1.difference(set2))  # Elements in set1 but not in set2

print(set1.symmetric_difference(set2))  # Elements in either set1 or set2 but not both